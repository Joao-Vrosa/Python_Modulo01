salario = float(input("Digite o valor do seu salario: "))

if salario > 1250:
    salario = salario + (salario * (10/100))
    print("Prabens! Você recebeu um aumento. Agora o seu salario é de R${}".format(salario))
else:
    salario = salario + (salario * (10/100))
    print("Prabens! Você recebeu um aumento. Agora o seu salario é de R${}".format(salario))

